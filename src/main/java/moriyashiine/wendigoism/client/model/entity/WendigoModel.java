	@Override
	public void setRotationAngles(@Nonnull T entity, float limbSwing, float limbSwingAmount, float ageInTicks, float netHeadYaw, float headPitch) {
		float right = MathHelper.cos(limbSwing * 0.6662f + (float) Math.PI) * limbSwingAmount * 0.5f;
		float left = MathHelper.cos(limbSwing * 0.6662f) * limbSwingAmount * 0.5f;
		if (entity.getDataManager().get(WendigoEntity.ATTACKING)) {
			right -= 1;
			left -= 1;
		}
		rArm00.rotateAngleX = right;
		lArm00.rotateAngleX = left;
		rLeg00.rotateAngleX = (MathHelper.cos(limbSwing * 0.6662f) * limbSwingAmount) - 0.5f;
		lLeg00.rotateAngleX = (MathHelper.cos(limbSwing * 0.6662f + (float) Math.PI) * limbSwingAmount) - 0.5f;
	}
	
